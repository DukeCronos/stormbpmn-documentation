</div>
<p>На этом экране можно настраивать:</p>
<ul>
<li>Отображение <RouteLink to="/features/#%D0%BC%D0%B5%D0%BD%D1%8E-%D0%BE%D0%B2%D0%B5%D1%80%D0%BB%D0%B5%D0%B5%D0%B2">оверлеев</RouteLink>  по ссылке</li>
<li>Просматривать, редактировать или удалять права конкретных пользователей на диаграмму</li>
<li>Предоставлять анонимный доступ</li>
<li>Предоставлять доступ к диаграмме для организации (только ENTERPRISE-версия)</li>
<li>Просматривать код для встраивания диаграммы в системы типа Confluence</li>
