<h2 id="_4-типа-совместнои-работы" tabindex="-1"><a class="header-anchor" href="#_4-типа-совместнои-работы"><span>4 типа совместной работы</span></a></h2>
<p>В системе существует несколько способов поделиться своей работой с коллегами - на уровне процесса, на уровне папки, на уровне команды и на уровне предприятия.</p>
<p>Чем ближе выданы права к диаграмме, тем они сильнее. Т.е. права на уровне процесса сильнее прав на уровне папки. А на уровне папки - сильнее чем на уровне команды.</p>
<h3 id="на-уровне-процесса-поделиться-конкретным-процессом" tabindex="-1"><a class="header-anchor" href="#на-уровне-процесса-поделиться-конкретным-процессом"><span>На уровне процесса - поделиться конкретным процессом</span></a></h3>
<p>Вы можете поделиться конкретной диаграммой с конкретными пользователями. Это хорошо работает, когда вам нужно просто поделиться одной диаграммой.
<img src="@source/team-work/share-diagram-1.png" alt="image"></p>
<div class="custom-container tip"><p class="custom-container-title">TIP</p>
