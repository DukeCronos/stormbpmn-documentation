<img src="@source/team-work/share-with-me.png" alt="image"></p>
<h3 id="на-уровне-папки" tabindex="-1"><a class="header-anchor" href="#на-уровне-папки"><span>На уровне папки</span></a></h3>
<p>В тарифе TEAM существует возможность поделится конкретной папкой с конкретным человеком. По смыслу работает точно так же  - отображается в &quot;поделились со мной&quot;, шлется нотификация. При этом человек НЕ увидит внутреннюю структуру папок, а просто увидит их все скопом.  Анонимный доступ до папок невозможен.
<img src="@source/team-work/share-folder.png" alt="image"></p>
<div class="custom-container tip"><p class="custom-container-title">TIP</p>
<p>Этот вариант хорошо работает, когда вы не хотите приглашать человека в вашу команду, но вам нужно поделиться с ним группой каких-то процессов.</p>
