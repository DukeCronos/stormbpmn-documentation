<h2 id="пулы" tabindex="-1"><a class="header-anchor" href="#пулы"><span>Пулы</span></a></h2>
<p>Пулы отображают участников взаимодействия СНАРУЖИ процесса -  можно понимать организации, отделы, процессы — это зависит от вашей точки зрения на процесс. Как правило, если вы рисуете сквозной процесс, у вас будет один пул для вашей организации и прочие — для ваших клиентов, партнёров и так далее.<br>
